package whiledemo.examples;

public class WhileLoopDemo {

	public static void main(String[] args) {
		
	  int n =1;
	
	  System.out.println("while loop start=============");
	
	  while(n <= 10) {
		  
		  System.out.println(n);
		  n++;
	  }
	  
	  System.out.println("while loop end==========");
		
	}
}
