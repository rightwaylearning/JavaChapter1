package Damini;

public class Incremant_Decrement {
	public static void main(String[] args) {
		
		int a=10;
		int b=a++;
		
		System.out.println(a); //a=11,b=10
		System.out.println(b);
	
		System.out.println("--------------------------");
	
		int c=20;
		int d=++c;
				
	   System.out.println(c);
	   System.out.println(d);
	   
	   System.out.println(".......................................");
		
	   System.out.println("Pre & post Increment");
	   
	   int e=20;
	   int f= --e;
	   
	   System.out.println(e);//ans e=19,f=19;
	   System.out.println(f);
	   
	   System.out.println("-----------------------------");
	   
	   int r=30;
	   int s=r--;
	   
	   System.out.println(r);//ans r=29,s=30;
	   System.out.println(s);
	   
	   System.out.println("-------------------------");
	   
	   System.out.println("pre & post decrement");
	   
	   
	   int x=20;
	   int y=x+1;
	   
	   System.out.println(x);
	   System.out.println(y);
	   
	 
	   
	}
	
	
	

}
