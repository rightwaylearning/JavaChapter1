package Akshay_Programs_on_Number;

public class Swapping {
	
	public static void main(String[] args) {
		
		int a = 10;
		int b = 20;
		
		int temp  = a; // Value is 10
		 a = b; // Value is 20
		 b = temp; // Value is 10
		
		
		System.out.println("a = " + a); // answer is = [ a = 20]
		System.out.println("b = " + b); // answer is = [ b = 10]
		
	}

}
