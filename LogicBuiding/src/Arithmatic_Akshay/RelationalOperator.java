package Arithmatic_Akshay;

public class RelationalOperator {

	public static void main(String[] args) {
		
		boolean a = 20000 > 20001;
		System.out.println("20000 > 20001 = " + a);
		System.out.println("---------------------");
		
		boolean b = 20000 < 20005;
		System.out.println("20000 < 20005 = " + b);
		System.out.println("---------------------");
		
		boolean c = 100 >= 200;
		System.out.println("100 >= 200 = " + c);
		System.out.println("---------------------");
		
		boolean d = 100 <= 200;
		System.out.println("100 <= 200 = " + d);
		System.out.println("---------------------");
		
	}
}
