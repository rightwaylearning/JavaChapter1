package Akshay_New_Package;

public class Downcasting2 {
	
	public static void main(String[] args) {
		
		int x = 10;
		byte z = (byte) x;
		System.out.println(z);
		
		int j = 280;
		byte k = (byte) j;
		System.out.println(k);
		
		int pj = 113;
		char ap = (char) pj;
		System.out.println(ap);
		
		int mn = 8596741;
		char pq = (char) mn;
		System.out.println(pq);
		
		byte sum = -57;
		char div = (char) sum;
		System.out.println(div);
	}
	
}
