package Nilam_2D_array;

public class Example_2D_Arry {
	public static void main(String[] args) {
		
		int[][] arr =new int[4][2];
		
		arr[0][0]= 10;
		arr[0][1]= 20;
		
		arr[1][0]= 30;
		arr[1][1]= 40;
		
		arr[2][0]= 50;
		arr[2][1]= 60;
		
		arr[3][0]= 70;
		arr[3][1]= 80;
		
		System.out.println(arr[2][1]);
	}
	
	

}
